def test_road_decode_phase_dependent() -> None:
    """Setup phases decode road indices to PlaceRoadAction; main phases to BuildRoadAction."""
    env = CatanEnv(seed=0)
    env.reset(seed=0)
    enc = ActionEncoder.for_state(env.state)

    # Drive to a state in INITIAL_ROAD by stepping the first legal action.
    while env.state.phase is not TurnPhase.INITIAL_ROAD:
        env.step(env.legal_actions()[0])
        if env.state.phase is TurnPhase.INITIAL_SETTLEMENT:
            continue

    decoded = enc.decode(0, env.state)
    assert isinstance(decoded, A.PlaceRoadAction)

    # Walk past setup into a non-setup phase.
    while env.state.phase in (TurnPhase.INITIAL_SETTLEMENT, TurnPhase.INITIAL_ROAD):
        env.step(env.legal_actions()[0])
    decoded = enc.decode(0, env.state)
    assert isinstance(decoded, A.BuildRoadAction)


def test_maritime_indices_have_distinct_resources() -> None:
    """All 20 maritime indices decode with give != receive."""
    env = CatanEnv(seed=0)
    enc = ActionEncoder.for_state(env.state)
    for offset in range(N_MARITIME_TRADES):
        idx = MARITIME_TRADE_START + offset
        # Decode requires we be in a state where the action class is well-defined;
        # decoding doesn't depend on phase for maritime, so use the fresh state.
        decoded = enc.decode(idx, env.state)
        assert isinstance(decoded, A.MaritimeTradeAction)
        assert decoded.give is not decoded.receive


def test_roll_index_decodes_to_roll_dice() -> None:
    env = CatanEnv(seed=0)
    enc = ActionEncoder.for_state(env.state)
    decoded = enc.decode(ROLL_INDEX, env.state)
    assert isinstance(decoded, A.RollDiceAction)


def _legal_actions(state: GameState) -> list[Action]:
    from domain.engine.game_engine import GameEngine
    from domain.engine.randomizer import SeededRandomizer

    return GameEngine(SeededRandomizer(0)).legal_actions(state)
