"""Round-trip and mask correctness for :class:`ActionEncoder`.

Property: for every reachable state, every action in ``legal_actions`` either
- round-trips through ``decode(encode(a), state) == a`` (strict equality), or
- belongs to a known equivalence class explicitly handled below
  (year-of-plenty: unordered pair; discard: collapsed to the sentinel
  trigger; domestic trade: not in the layout, encoder raises ``ValueError``).

State coverage is generated by random rollouts, which is the closest the
project has to a stationary distribution over reachable positions; 50 seeds ×
100 steps ≈ 5 000 visited states.
"""

from __future__ import annotations

import random

import numpy as np
import pytest
from hypothesis import given, settings, strategies as st

from domain.actions import all_actions as A
from domain.actions.base import Action
from domain.engine.game_engine import IllegalActionError
from domain.enums import Resource, TurnPhase
from domain.game.state import GameState
from rl.encoding._action_layout import (
    _ACTION_LAYOUT_VERSION,
    ACTION_SPACE_SIZE,
    DISCARD_INDEX,
    MARITIME_TRADE_PAIRS,
    MARITIME_TRADE_START,
    N_MARITIME_TRADES,
    N_YEAR_OF_PLENTY,
    ROLL_INDEX,
    YOP_PAIRS,
)
from rl.encoding.action import ActionEncoder, DiscardSentinel
from rl.env.catan_env import CatanEnv


