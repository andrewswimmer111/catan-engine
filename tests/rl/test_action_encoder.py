@settings(max_examples=50, deadline=None)
@given(st.integers(min_value=0, max_value=10_000))
def test_round_trip_over_reachable_states(seed: int) -> None:
    states = _rollout_states(seed, max_steps=120)
    for state in states:
        enc = ActionEncoder.for_state(state)
        for action in _legal_actions(state):
            if isinstance(action, _DOMESTIC_TRADE_TYPES):
                continue  # not in layout; encoder raises by design
            idx = enc.encode(action)
            assert 0 <= idx < ACTION_SPACE_SIZE

            decoded = enc.decode(idx, state)

            if isinstance(action, A.DiscardResourcesAction):
                # Combinatorial payload is collapsed to the sentinel.
                assert idx == DISCARD_INDEX
                assert isinstance(decoded, DiscardSentinel)
                assert decoded.player_id == action.player_id
                continue

            assert _actions_equivalent(decoded, action), (
                f"round-trip failed: {action!r} -> {idx} -> {decoded!r}"
            )


@settings(max_examples=30, deadline=None)
@given(st.integers(min_value=0, max_value=10_000))
def test_mask_cardinality_matches_distinct_encoded_indices(seed: int) -> None:
    states = _rollout_states(seed, max_steps=60)
    for state in states:
        enc = ActionEncoder.for_state(state)
        legal = _legal_actions(state)
        encodable = [a for a in legal if not isinstance(a, _DOMESTIC_TRADE_TYPES)]
        m = enc.mask(legal)
        assert m.shape == (ACTION_SPACE_SIZE,)
        assert m.dtype == np.bool_
        expected = {enc.encode(a) for a in encodable}
        assert int(m.sum()) == len(expected)
        for idx in expected:
            assert m[idx]


@settings(max_examples=20, deadline=None)
@given(st.integers(min_value=0, max_value=10_000))
def test_decoded_index_outside_mask_is_rejected_by_engine(seed: int) -> None:
    """An index whose mask bit is False decodes to an action the engine refuses.

    Excludes the discard sentinel (not an Action) and indices that decode to
    something whose engine validation raises a TypeError or KeyError before
    the legality check (none currently, but worth pinning).
    """
    from domain.engine.game_engine import GameEngine
    from domain.engine.randomizer import SeededRandomizer

    states = _rollout_states(seed, max_steps=40)
    rng = random.Random(seed)
    for state in states:
        enc = ActionEncoder.for_state(state)
        m = enc.mask(_legal_actions(state))
        false_indices = [i for i in range(ACTION_SPACE_SIZE) if not m[i] and i != DISCARD_INDEX]
        if not false_indices:
            continue
        # Sample a handful per state — exhaustive scan would be slow.
        for idx in rng.sample(false_indices, k=min(8, len(false_indices))):
            try:
                decoded = enc.decode(idx, state)
            except ValueError:
                # Out-of-range steal seats etc. — fine; never reach engine.
                continue
            if isinstance(decoded, DiscardSentinel):
                continue
            engine = GameEngine(SeededRandomizer(seed))
            with pytest.raises(IllegalActionError):
                engine.apply_action(state, decoded)


