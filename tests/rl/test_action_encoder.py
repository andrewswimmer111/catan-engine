def test_decode_out_of_range_raises() -> None:
    env = CatanEnv(seed=0)
    enc = ActionEncoder.for_state(env.state)
    with pytest.raises(ValueError):
        enc.decode(-1, env.state)
    with pytest.raises(ValueError):
        enc.decode(ACTION_SPACE_SIZE, env.state)


def test_encode_unsupported_action_raises() -> None:
    enc = ActionEncoder([])
    fake = A.ProposeDomesticTradeAction(
        player_id=0,  # type: ignore[arg-type]
        offer={},
        request={},
    )
    with pytest.raises(ValueError):
        enc.encode(fake)


