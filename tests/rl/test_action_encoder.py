# Domestic-trade actions are not in the layout (rl-005); the encoder raises
# ValueError for them. The property test filters them out before the
# round-trip check.
_DOMESTIC_TRADE_TYPES: tuple[type, ...] = (
    A.ProposeDomesticTradeAction,
    A.RespondDomesticTradeAction,
    A.ConfirmDomesticTradeAction,
    A.CancelDomesticTradeAction,
)


def _yop_unordered(a: A.PlayYearOfPlentyAction) -> frozenset[Resource]:
    return frozenset((a.resource1, a.resource2)) if a.resource1 is not a.resource2 else frozenset({a.resource1})


def _actions_equivalent(a: Action, b: Action) -> bool:
    """Equality up to known semantic-equivalence classes.

    PlayYearOfPlentyAction is order-insensitive in (resource1, resource2);
    the encoder canonicalises to sorted-by-enum form during decode, so
    ``PlayYOP(WOOD, BRICK)`` may round-trip as ``PlayYOP(BRICK, WOOD)``
    (engine emits both orderings).
    """
    if isinstance(a, A.PlayYearOfPlentyAction) and isinstance(
        b, A.PlayYearOfPlentyAction
    ):
        if a.player_id != b.player_id:
            return False
        return _yop_unordered(a) == _yop_unordered(b)
    return a == b


def _rollout_states(seed: int, max_steps: int) -> list[GameState]:
    """Drive ``CatanEnv`` with uniform-random legal moves and snapshot states.

    Yields ``max_steps + 1`` states (initial plus one per applied action), or
    fewer if the game terminates earlier.
    """
    env = CatanEnv(seed=seed)
    env.reset(seed=seed)
    rng = random.Random(seed)
    states: list[GameState] = [env.state]
    for _ in range(max_steps):
        legal = env.legal_actions()
        if not legal:
            break
        action = rng.choice(legal)
        _obs, _r, done, _info = env.step(action)
        states.append(env.state)
        if done:
            break
    return states


