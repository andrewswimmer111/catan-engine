def test_action_layout_version_pinned() -> None:
    assert _ACTION_LAYOUT_VERSION == 1


def test_action_space_size_matches_layout_table() -> None:
    # Sum of widths in the layout table.
    expected = (
        72  # roads
        + 54  # settlements
        + 54  # cities
        + 19  # robber moves
        + 4   # steal seats
        + 20  # maritime trades
        + 1   # roll
        + 1   # end turn
        + 1   # buy dev
        + 2   # knight + road building
        + 5   # monopoly
        + 15  # year of plenty
        + 1   # discard trigger
    )
    assert ACTION_SPACE_SIZE == expected
    assert ACTION_SPACE_SIZE == 249


def test_maritime_trade_pairs_have_no_self_trades() -> None:
    assert len(MARITIME_TRADE_PAIRS) == N_MARITIME_TRADES == 20
    for give, receive in MARITIME_TRADE_PAIRS:
        assert give is not receive


def test_year_of_plenty_pairs_are_unordered_and_unique() -> None:
    assert len(YOP_PAIRS) == N_YEAR_OF_PLENTY == 15
    seen_unordered: set[frozenset[Resource]] = set()
    for r1, r2 in YOP_PAIRS:
        key = frozenset({r1, r2}) if r1 is not r2 else frozenset({r1})
        assert key not in seen_unordered, f"duplicate unordered pair {(r1, r2)}"
        seen_unordered.add(key)


